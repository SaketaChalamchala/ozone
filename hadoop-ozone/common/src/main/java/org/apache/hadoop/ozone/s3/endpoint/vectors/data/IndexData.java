package org.apache.hadoop.ozone.s3.endpoint.vectors.data;

import java.util.List;

public class IndexData {

  private String indexName;
  private List<Float> embedding;
  private String metadata;

  public IndexData(String indexName, List<Float> embedding, String metadata) {
    this.indexName = indexName;
    this.embedding = embedding;
    this.metadata = metadata;
  }

  public String getIndexName() {
    return indexName;
  }

  public void setIndexName(String indexName) {
    this.indexName = indexName;
  }

  public List<Float> getEmbedding() {
    return embedding;
  }

  public void setEmbedding(List<Float> embedding) {
    this.embedding = embedding;
  }

  public String getMetadata() {
    return metadata;
  }

  public void setMetadata(String metadata) {
    this.metadata = metadata;
  }

  @Override
  public String toString() {
    return String.format("IndexData{\nindexName='%s',\n embedding='%s',\n metadata=%s}",
        indexName, embedding, metadata);
  }
}
